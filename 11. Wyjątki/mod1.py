def dodawanie(a: float,b: float) -> float:
    """wykonuje dodawanie"""
    return a + b

def odejmowanie(a: float,b:float) -> float:
    """wykonuje odejmowanie"""
    return a - b

def mnozenie(a:float,b:float) -> float:
    """wykonuje mnozenie"""
    return a*b

def dzielenie(a:float,b:float) -> float:
    """wykonuje dzielenie"""
    return a / b
